#include <stdio.h>
#include <stdlib.h>
#include "calculator.h"

// Function Defintions

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Add_Numbers
 *  Description:  Add numbers
 *  return; total
 * =====================================================================================
 */
int Add_Numbers(int num1, int num2)
{
    return num1 + num2;
}

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Sub_Numbers
 *  Description:  subtraction of number
 *  return; total
 * =====================================================================================
 */
int Sub_Numbers(int num1, int num2)
{
return num1 - num2;
}
